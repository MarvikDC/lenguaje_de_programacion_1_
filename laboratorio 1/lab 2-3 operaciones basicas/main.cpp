#include <iostream>

using namespace std;

int main()
{
    int a;
    int b;
    cout<<"primer numero"<<endl;
    cin>>a;
    cout<<"segundo numero"<<endl;
    cin>>b;
    cout<<"operaciones con enteros "<<endl;
    cout<<"suma: "<<a+b<<endl;
    cout<<"resta: "<<a-b<<endl;
    cout<<"multiplicacion: "<<a*b<<endl;
    cout<<"dividision: "<<a/b<<endl;

    cout<<"operaciones con decimales "<<endl;
    double c=a;
    double d=b;
    cout<<"suma: "<<c+d<<endl;
    cout<<"resta: "<<c-d<<endl;
    cout<<"multiplicacion: "<<c*d<<endl;
    cout<<"dividision: "<<c/d<<endl;


    return 0;
}
